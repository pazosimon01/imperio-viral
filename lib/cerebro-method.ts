// Contexto de negocio que el usuario carga en el formulario.
export interface BusinessBrief {
  nombre: string; // marca o negocio
  sector: string; // nicho/sector
  oferta: string; // qué vende
  clienteIdeal: string;
  objetivo: string; // qué quiere conseguir (se traduce a creencia)
  notas?: string | null; // voz, pruebas reales, restricciones
}

export function businessBlock(b: BusinessBrief): string {
  return `NEGOCIO/MARCA:
- Nombre: ${b.nombre}
- Sector/nicho: ${b.sector}
- Qué vende (oferta): ${b.oferta}
- Cliente ideal: ${b.clienteIdeal}
- Objetivo declarado: ${b.objetivo}
${b.notas ? `- Notas del dueño (voz, pruebas reales, límites): ${b.notas}` : ""}
RECUERDA ZERO-INVENTION: solo puedes usar como "prueba" lo que está en estas notas. Todo lo demás va generalizado o con [PLACEHOLDER].`;
}
